void Auxiliar::drawBoundingBox(){
    ofPushStyle();
    ofSetColor(255, 0, 0);
    ofSetLineWidth(3);
    ofDrawLine(0, 1, 1920, 1);
    ofDrawLine(0, 1079, 1920, 1079);
    ofDrawLine(1, 0, 1, 1080);
    ofDrawLine(1919, 0, 1919, 1080);
    ofPopStyle();
}

